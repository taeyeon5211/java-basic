package day04;

public class CompareOperatorEx {
    public static void main(String[] args) {
        int n1 = 10;
        int n2 = 10;
        boolean result1;

        result1 = n1 == n2;
        System.out.println(result1);
        result1 = n1 != n2;
        System.out.println(result1);
        result1 = n1 <= n2;
        System.out.println(result1);
        result1 = n1 >= n2;
        System.out.println(result1);
        result1 = n1 < n2;
        System.out.println(result1);
        result1 = n1 > n2;
        System.out.println(result1);
    }
}
